%% ==================== CLASS 2: Extract from .mat ====================

function PF = generateClass2PF(problemName, targetN)
%GENERATECLASS2PF Extract ~targetN uniformly distributed points from stored .mat file.

    % Determine benchmark suite and locate .mat file
    matPath = locateMatFile(problemName);

    if isempty(matPath) || ~exist(matPath, 'file')
        fprintf('WARNING - .mat file not found for %s. ', problemName);
        PF = [];
        return;
    end

    % Load PF data (handle different variable names)
    data = load(matPath);
    if isfield(data, 'F')
        rawPF = data.F;
    elseif isfield(data, 'PF')
        rawPF = data.PF;
    else
        % Try first field
        fields = fieldnames(data);
        if ~isempty(fields)
            rawPF = data.(fields{1});
        else
            fprintf('WARNING - no data in %s. ', matPath);
            PF = [];
            return;
        end
    end

    fprintf('(loaded %d raw points) ', size(rawPF, 1));

    if size(rawPF, 1) <= targetN
        PF = rawPF;
        return;
    end

    % Identify corner solutions (min on each objective)
    M = size(rawPF, 2);
    cornerIdx = zeros(M, 1);
    for j = 1:M
        [~, cornerIdx(j)] = min(rawPF(:, j));
    end
    cornerIdx = unique(cornerIdx);
    numCorners = numel(cornerIdx);

    % Separate corners from candidates
    allIdx = (1:size(rawPF, 1))';
    candidateIdx = setdiff(allIdx, cornerIdx);
    candidates = rawPF(candidateIdx, :);

    % Select remaining points uniformly
    numToSelect = targetN - numCorners;
    if numToSelect > 0 && size(candidates, 1) > numToSelect
        [selectedSubIdx, ~] = getLeastCrowdedPoints(candidates, numToSelect);
        selectedIdx = candidateIdx(selectedSubIdx);
    elseif numToSelect > 0
        selectedIdx = candidateIdx;
    else
        selectedIdx = [];
    end

    % Combine corners + selected
    finalIdx = [cornerIdx; selectedIdx];
    PF = rawPF(finalIdx, :);
end

function matPath = locateMatFile(problemName)
%LOCATEMATFILE Find the .mat file for a given problem name.
    baseDir = fileparts(fileparts(fileparts(mfilename('fullpath'))));
    probDir = fullfile(baseDir, 'Problems', 'Multi-objective optimization');

    % Map problem names to benchmark suite directories
    % Note: MOTSP/MOKP are handled directly by generateApproximatePF
    % and no longer route through locateMatFile.
    if startsWith(problemName, 'MinusWFG')
        suite = 'MinusWFG';
    elseif startsWith(problemName, 'MinusDTLZ')
        suite = 'MinusDTLZ';
    elseif startsWith(problemName, 'WFG')
        suite = 'WFG';
    elseif startsWith(problemName, 'RWA')
        suite = 'RWA';
    else
        suite = problemName;
    end

    matPath = fullfile(probDir, suite, [problemName '.mat']);
end
